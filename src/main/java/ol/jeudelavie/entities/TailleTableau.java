package ol.jeudelavie.entities;

public class TailleTableau {
	
	private int x;
	private int y;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public TailleTableau(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public TailleTableau() {
		super();
	}
	
	

}
